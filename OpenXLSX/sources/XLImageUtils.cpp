/**
 * @brief Get image dimensions from binary data
 * @param data Binary image data
 * @param mimeType MIME type of the image
 * @return Pair of (width, height) in pixels
 */
std::pair<uint32_t, uint32_t> XLImageUtils::getImageDimensions(const std::vector<uint8_t>& data, XLMimeType mimeType)
{
    if (data.size() < 8) return {0, 0};

    // PNG signature check and dimension extraction
    if (mimeType == XLMimeType::PNG) {
        if (data.size() >= 24 && data[0] == 0x89 && data[1] == 0x50 && data[2] == 0x4E && data[3] == 0x47) {
            // PNG IHDR chunk contains width and height at bytes 16-23
            uint32_t width = (data[16] << 24) | (data[17] << 16) | (data[18] << 8) | data[19];
            uint32_t height = (data[20] << 24) | (data[21] << 16) | (data[22] << 8) | data[23];
            return {width, height};
        }
    }
    // JPEG signature check and dimension extraction
    else if (mimeType == XLMimeType::JPEG) {
        if (data.size() >= 2 && data[0] == 0xFF && data[1] == 0xD8) {
            // For JPEG, we need to find the SOF0 marker (0xFFC0) and extract dimensions
            for (size_t i = 2; i < data.size() - 8; ++i) {
                if (data[i] == 0xFF && data[i + 1] == 0xC0) {
                    uint32_t height = (data[i + 5] << 8) | data[i + 6];
                    uint32_t width = (data[i + 7] << 8) | data[i + 8];
                    return {width, height};
                }
            }
        }
    }
    // BMP signature check and dimension extraction
    else if (mimeType == XLMimeType::BMP) {
        if (data.size() >= 26 && data[0] == 0x42 && data[1] == 0x4D) {
            // BMP header contains width and height at bytes 18-25
            uint32_t width = data[18] | (data[19] << 8) | (data[20] << 16) | (data[21] << 24);
            uint32_t height = data[22] | (data[23] << 8) | (data[24] << 16) | (data[25] << 24);
            return {width, height};
        }
    }
    // GIF signature check and dimension extraction
    else if (mimeType == XLMimeType::GIF) {
        if (data.size() >= 10 && data[0] == 0x47 && data[1] == 0x49 && data[2] == 0x46) {
            // GIF header contains width and height at bytes 6-9
            uint32_t width = data[6] | (data[7] << 8);
            uint32_t height = data[8] | (data[9] << 8);
            return {width, height};
        }
    }

    // Default fallback - assume square image
    return {100, 100};
}

/**
 * @brief Get image dimensions from binary data (auto-detect MIME type)
 * @param data Binary image data
 * @return Pair of (width, height) in pixels, or {0,0} if detection fails
 */
std::pair<uint32_t, uint32_t> XLImageUtils::getImageDimensions(const std::vector<uint8_t>& data)
{
    // Auto-detect MIME type first
    const XLMimeType mimeType = detectMimeType(data);
    if (mimeType == XLMimeType::Unknown) {
        return {0, 0};
    }
    
    // Use the existing function with detected MIME type
    return getImageDimensions(data, mimeType);
}

/**
 * @brief Check if image dimensions are reasonable
 * @param width Width in pixels
 * @param height Height in pixels
 * @return True if dimensions are within reasonable bounds
 */
bool XLImageUtils::isValidImageSize(uint32_t width, uint32_t height)
{
    // Reasonable bounds: 1x1 to 10000x10000 pixels
    return width > 0 && height > 0 && width <= 10000 && height <= 10000;
}

/**
 * @brief Validate image data integrity
 * @param data Binary image data
 * @param mimeType MIME type of the image
 * @return True if image data is valid
 */
bool XLImageUtils::validateImageData(const std::vector<uint8_t>& data, const XLMimeType& mimeType)
{
    if (data.empty()) return false;
    
    // Check basic signature matches MIME type
    if (XLMimeType::PNG == mimeType) {
        return data.size() >= 8 && data[0] == 0x89 && data[1] == 0x50 && data[2] == 0x4E && data[3] == 0x47;
    }
    if (XLMimeType::JPEG == mimeType) {
        return data.size() >= 2 && data[0] == 0xFF && data[1] == 0xD8;
    }
    if (XLMimeType::BMP == mimeType) {
        return data.size() >= 2 && data[0] == 0x42 && data[1] == 0x4D;
    }
    if (XLMimeType::GIF == mimeType) {
        return data.size() >= 3 && data[0] == 0x47 && data[1] == 0x49 && data[2] == 0x46;
    }
    
    return false;
}

/**
 * @brief Calculate hash value for binary image data
 * @param imageData Binary image data as vector<uint8_t>
 * @return Hash value for the image data
 */
size_t XLImageUtils::imageDataHash(const std::vector<uint8_t>& imageData)
{
    std::string imageDataStr;
    imageDataStr.reserve(imageData.size());
    imageDataStr.insert(imageDataStr.end(), imageData.begin(), imageData.end());
    const size_t h = imageDataStrHash(imageDataStr);
    return h;
}

/**
 * @brief Calculate hash value for image data stored as string
 * @param imageDataStr Binary image data as string
 * @return Hash value for the image data (same as imageDataHash for equivalent data)
 */
size_t XLImageUtils::imageDataStrHash(const std::string& imageDataStr)
{
    size_t h = 0;
    if (!imageDataStr.empty()) {
        std::hash<std::string> hasher;
        h = hasher(imageDataStr);
    }
    return h;
}
